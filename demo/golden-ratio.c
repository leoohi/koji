#define CINT_SCALE 300

#include "cint.c"
#include "stdio.h"

int main(void) {
    cint five, one;
    cint_init(&one, 10);
    cint_powi(&one, 1000);
    cint_init(&five, 10);
    cint_powi(&five, 2000);
    cint_nat_muli(&five, 5);
    cint_sqrti(&five);
    cint_addi(&five, &one);
    cint_down2i(&five);
    char *str = cint_to_string(&five, 10);
    str[1000] = 0;
    printf("golden ratio = %c.%s", *str, str + 1);
    free(str);
}
