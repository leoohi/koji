#define CINT_SCALE 150

#include "cint.c"
#include "stdio.h"

int main(void) {
    cint two_raised, n_1, n_2, sum;
    cint_init(&sum, 0);
    cint_init(&two_raised, 10);
    cint_powi(&two_raised, 1200);
    cint_up2i(&two_raised); // 2 * 10^1200
    for (int k = 0; k < 800; ++k) {
        cint_init(&n_1, 25);
        cint_powi(&n_1, k);
        cint_nat_muli(&n_1, 5 * (k + k + 1));
        n_2 = two_raised;
        cint_divi(&n_2, &n_1);
        cint_addi(&sum, &n_2);
        cint_init(&n_1, 49);
        cint_powi(&n_1, k);
        cint_nat_muli(&n_1, 7 * (k + k + 1));
        n_2 = two_raised;
        cint_divi(&n_2, &n_1);
        cint_addi(&sum, &n_2);
    }
    char *str = cint_to_string(&sum, 10);
    str[1000] = 0;
    printf("ln(2) = 0.%s", str);
    free(str);
}
